#!/usr/bin/env bash
# =============================================================================
# Kubernetes in Action Ch2 §2.3 — 네임스페이스·cgroup으로 보는 컨테이너 격리
#
# "컨테이너 = 여러 네임스페이스가 배정된 프로세스"임을 살아있는 컨테이너로 직접
# 확인합니다. PID/UTS 네임스페이스, 두 컨테이너 대조(격리 vs 공유), cgroup 강제를
# 눈으로 봅니다.
#
# 전제: docker 실행 중. kiada 이미지(02-02)가 있으면 그걸 쓰고, 없으면 nginx 로 대체.
# 학습 노트(개념): runners-high/write/08_cloud/book/kubernetes-in-action/02-03
#
# ⚠️ 블록 단위로 따라 하며 출력을 관찰하세요. macOS 라면 docker exec 안(리눅스 VM)에서
#    /proc 를 읽어야 합니다 — 호스트 macOS 의 /proc 는 리눅스 컨테이너와 무관합니다.
# =============================================================================
set -euo pipefail

# -----------------------------------------------------------------------------
# 0. 대조용 컨테이너 두 개 띄우기 (kiada 와 web1)
#    네임스페이스 격리는 "한 컨테이너만 봐서는" 증명이 안 된다. 두 컨테이너의
#    네임스페이스 번호를 '대조'해야 다른지(격리) 같은지(공유) 알 수 있다.
# -----------------------------------------------------------------------------
docker run -d --name kiada-container kiada:0.1 2>/dev/null \
  || docker run -d --name kiada-container nginx:alpine
docker run -d --name web1 nginx:alpine

# -----------------------------------------------------------------------------
# 1. PID 네임스페이스 — 같은 프로세스가 안/밖에서 다른 PID
#    컨테이너 안에선 PID 1(자기가 첫 프로세스), 호스트에선 큰 번호.
# -----------------------------------------------------------------------------
docker exec kiada-container ps -o pid,comm | head        # 안: PID 1
docker inspect --format '{{.State.Pid}}' kiada-container  # 밖: 예) 32687

# -----------------------------------------------------------------------------
# 2. UTS 네임스페이스 — 호스트명이 컨테이너 ID (독립 정체성)
#    02-02 의 "Request processed by <컨테이너ID>" 가 바로 이 호스트명이다.
# -----------------------------------------------------------------------------
docker exec kiada-container hostname                      # 예: fe6daee29272

# -----------------------------------------------------------------------------
# 3. 네임스페이스 대조 — 번호가 다르면 격리, 같으면 공유
#    /proc/1/ns/* 심볼릭 링크의 번호를 두 컨테이너에서 비교한다.
#    실측 결과:
#      uts/pid/net  → 두 컨테이너가 '다름'  = 격리 (각자 호스트명·PID·네트워크)
#      user/time    → 두 컨테이너가 '같음'  = 공유 (Docker 기본은 호스트 것을 씀)
# -----------------------------------------------------------------------------
for c in kiada-container web1; do
  echo "== $c =="
  docker exec "$c" sh -c 'for ns in uts pid net user time; do echo "$ns -> $(readlink /proc/1/ns/$ns)"; done'
done
#   예)               kiada            web1          판정
#     uts     [4026534383]     [4026534113]     다름 → 격리
#     pid     [4026534385]     [4026534115]     다름 → 격리
#     net     [4026534387]     [4026534117]     다름 → 격리
#     user    [4026531837]     [4026531837]     같음 → 공유
#     time    [4026531834]     [4026531834]     같음 → 공유
#   낮은 번호(40265318xx)=호스트 기본 ns(공유), 높은 번호(40265343xx)=새로 만든 ns(격리)

# -----------------------------------------------------------------------------
# 4. cgroup — "얼마나 쓸 수 있나". --memory 옵션이 커널 파일에 그대로 박힌다.
#    네임스페이스가 '무엇을 볼 수 있나'라면, cgroup 은 '얼마나 쓸 수 있나'다.
# -----------------------------------------------------------------------------
docker run -d --name cg-test --memory="100m" nginx:alpine
docker inspect --format '{{.HostConfig.Memory}}' cg-test   # 104857600 (=100MB)
docker exec cg-test cat /sys/fs/cgroup/memory.max          # 104857600 (동일값!)
#   docker run --memory → docker inspect → 컨테이너 안 memory.max, 세 값이 일치.
#   즉 내가 건 옵션이 커널 cgroup 파일에 박히고, 커널이 그 파일로 메모리를 강제한다.

# -----------------------------------------------------------------------------
# 5. 정리
# -----------------------------------------------------------------------------
# docker rm -f kiada-container web1 cg-test
